/** หาหัวข้อจาก slug (ไม่เจอ = หัวข้อแรกสุด) */
export function findTopic(slug?: string): HelpTopic {
  for (const g of HELP_GROUPS) {
    const t = g.topics.find((t) => t.slug === slug);
    if (t) return t;
  }
  return HELP_GROUPS[0].topics[0];
}
